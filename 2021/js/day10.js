Array.prototype.last = function() {
	return this.length > 0 ? this[this.length - 1] : undefined
}

const badLines = (a) => {
	const openers = ['(','[','{','<']
	const closers = [')',']','}','>']
  const scores = [3, 57, 1197, 25137]
  let badScore = 0
  let incompleteScore = 0
  const incompletes = []
	for (let i = 0; i < a.length; i++) {
  	const q = []
    let broken = false
    for (let j = 0; j < a[i].length; j++) {
    	const c = a[i][j]
      // an opening bracket, add it to the queue
    	if (openers.includes(c)) {
      	q.push(c)
      }
      if (closers.includes(c)) {
      	// if the closing bracket matches the last opening bracket
      	if (openers.indexOf(q.last()) === closers.indexOf(c)) {
        	q.pop()
          continue
        }
				// bracket mismatch, note the error and go the next line
        broken = true
        badScore += scores[closers.indexOf(c)]
        break
      }
    }
    if (!broken) {
      let s = 0
      while (q.length > 0) {
        s *= 5
        s += openers.indexOf(q.pop()) + 1
      }
      incompletes.push(s)
    }
  }
  
  const c = Math.ceil(incompletes.length / 2) - 1
  incompleteScore = incompletes.length ? incompletes.sort((a, b) => a - b)[c] : 0
  
  return {
  	badScore,
  	incompleteScore
  }
}

